function somar(){
    let numero1, numero2, soma 

    numero1 = Number (prompt("Digite o primeiro número: "))
    numero2 = Number (prompt("Digite o primeiro número: "))

    soma = numero1 + numero2

    alert ("Soma: " + soma)
}
function subtrair(){
    let numero1, numero2, produto 

    numero1 = Number (prompt("Digite o primeiro número: "))
    numero2 = Number (prompt("Digite o primeiro número: "))

    produto = numero1 - numero2

    alert ("Subtração: " + produto)
}